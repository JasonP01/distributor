package com.xpdustry.distributor.api.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a method as a player action handler, meaning it will be called by {@link mindustry.net.Administration#allowAction}.
 * <br>
 * The annotated method must have a single {@link mindustry.net.Administration.PlayerAction} parameter,
 * and return a {@code boolean}, whether the action should be allowed or not.
 * @since 4.2.0
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface PlayerActionHandler {}
